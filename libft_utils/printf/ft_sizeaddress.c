#include "ft_printf.h"

size_t	ft_sizeaddress(size_t n)
{
	size_t	size;

	size = 2;
	if (n == 0)
		return (5);
	while (n != 0)
	{
		n /= 16;
		size++;
	}
	return (size);
}
